package com.learnjava.topic13.statickeyword;

public class Demo_ClassAndObject_001_Attribute {
	
	// Attributes
		public static  int noOfBooks;
		public static float price ;
		public static double weight;
		public static char grade;
		public static boolean hasAllPages;
		public static String name;
		
		

	public static void main(String[] args) {
		
		System.out.println("Default of value of int attribute is "+noOfBooks );

		System.out.println("Default of value of float attribute is "+price );

		System.out.println("Default of value of double attribute is "+weight );
		
		System.out.println("Default of value of char attribute is "+grade );
		
		System.out.println("Default of value of boolean attribute is "+hasAllPages );
		
		System.out.println("Default of value of String(Derived Data Type) attribute is "+name );

		// 	Default value of derived data type is null . 
 

	}

}
