package com.companyname.demo3.stringbuilder;

public class Demo_StringBuilder_4 {

	public static void main(String[] args) {
		String str =new String("String");
		StringBuffer str1 =new StringBuffer("StringBuffer");
		StringBuilder str3 =new StringBuilder("StringBuilder");
		str.toString();
		
		//StringBuffer to string
		str1.toString();
		//StringBuilder to String
		str3.toString();
		
	}

}
