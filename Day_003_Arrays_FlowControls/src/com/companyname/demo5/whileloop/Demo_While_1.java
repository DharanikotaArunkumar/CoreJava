package com.companyname.demo5.whileloop;



public class Demo_While_1 {

	public static void main(String[] args) {
		
		// while is used to do some recursive action 
		// we should use the while when we do not know the exact iterations
		int value =1; // Declaration		
		
		while (value<=8){ // condition Checking
			System.out.println("Value of i is :"+value);
			value++;		// increment	
		}
		
		
	}

}
