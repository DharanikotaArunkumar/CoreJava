package com.companyname.demo8.booleanoperator;

public class Demo_Boolean_1 {

	public static void main(String[] args) {
		boolean a=true;
		boolean b=true;
		if(a&b){
			System.out.println("true");
		}
		
		
		if(a|b){
			System.out.println("true");
		}
		a=false;
		
		if(!a){
			System.out.println("true");
		}
		
	}

}
