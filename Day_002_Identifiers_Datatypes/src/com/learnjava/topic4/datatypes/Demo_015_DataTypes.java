/*
 * Different types of data types
 */
package com.learnjava.topic4.datatypes;

public class Demo_015_DataTypes {

	public static void main(String[] args) {
		byte age =23;
		System.out.println("age is "+age);
		
		short colorCode =452;
		System.out.println("colorCode is "+colorCode);
		
		int value =100;
		System.out.println("value is "+value);
		
		
		long earthAndMoonDistance = 1231123312L;
		System.out.println("earthAndMoonDistance is "+earthAndMoonDistance);
		
		float temparature = 3.14f;
		System.out.println("temparature is "+temparature);
		
		double piValue = 3.142311411d;
		System.out.println("piValue is "+piValue);
		

	}

}
